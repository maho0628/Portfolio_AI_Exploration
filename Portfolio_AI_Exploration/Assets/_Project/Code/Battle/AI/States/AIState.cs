/// <summary>
/// 戦闘AIの思考フェーズを表す状態定義。
/// 
/// 各状態は Tick() 内で評価され、
/// 状況に応じて遷移することでAIの行動が決定される。
/// </summary>
public enum AIState
{
    Idle,//待機状態。次の行動を決定する前の基本状態
    Hold, //スキル準備状態    行動ゲージ満タン時
    Attack,//通常攻撃実行中
    Skill,//プレイヤー入力によるスキル発動中	
    Investigate,// 特殊行動・イベント用状態
    Dead//戦闘不能
}

